#include "libft.h"

void		ft_putnbr(int n)
{
	unsigned int i;

	if (n < 0)
	{
		n = -n;
		ft_putchar('-');
	}
	i = (unsigned int)n;
	if (i > 9)
		ft_putnbr(i / 10);
	ft_putchar((i % 10) + 48);
}

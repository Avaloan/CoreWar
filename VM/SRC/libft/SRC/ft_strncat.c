#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	size_t	k;

	i = 0;
	k = 0;
	while (s1[i] != '\0')
		i++;
	while (k < n && s2[k])
		s1[i++] = s2[k++];
	s1[i] = '\0';
	return (s1);
}

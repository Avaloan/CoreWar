#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int k;

	i = 0;
	k = 0;
	while (s1[k] != '\0')
		k++;
	while (s2[i] != '\0')
	{
		s1[k + i] = s2[i];
		i++;
	}
	s1[k + i] = '\0';
	return (s1);
}

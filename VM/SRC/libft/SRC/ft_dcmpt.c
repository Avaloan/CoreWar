#include "libft.h"

void	ft_dcmpt(int nb)
{
	int i;

	i = 2;
	while (nb != 1)
	{
		i = 2;
		while ((nb % i) != 0)
			i++;
		ft_putnbr(i);
		if ((nb / i) != 1 || (nb % i) != 0)
			ft_putchar('x');
		nb = (nb / i);
	}
	ft_putchar('\n');
}

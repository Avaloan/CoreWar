#include "libft.h"

int	ft_countiter(char *str, char c)
{
	int i;
	int k;

	k = 0;
	i = -1;
	if (!(str))
		return (0);
	while (str[++i] != '\0')
	{
		if (str[i] == c)
			k++;
	}
	return (k);
}

#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*ss;

	ss = (char *)(s);
	i = 0;
	while (i < n)
	{
		*ss = 0;
		i++;
		ss++;
	}
}
